#include "libft.h"

int	ft_strcspn(const char *str1, const char *str2)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str1[i])
	{
		while (str2[j])
		{
			if (str1[i] == str2[j])
				return (i);
			j++;
		}
		j = 0;
		i++;
	}
	return (-1);
}
